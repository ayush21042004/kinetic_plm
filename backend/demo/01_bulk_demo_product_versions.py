def _series_code(code_prefix: str, sequence: int) -> str:
    return f"{code_prefix}-{sequence:03d}"


def _build_record(xml_id: str, name: str, code: str, description: str, sale_price: int, cost_price: int) -> dict:
    return {
        "model": "product.version",
        "values": {
            "version": 1,
            "name": name,
            "default_code": code,
            "description": description,
            "state": "active",
            "sale_price": sale_price,
            "cost_price": cost_price,
        },
    }


def _build_records() -> dict:
    records = {}
    counter = 1

    for series in SERIES_DEFINITIONS:
        for variant_name, sale_price, cost_price in series["variants"]:
            xml_id = f"demo_version_{series['key']}_{counter:03d}"
            product_name = f"{series['label']} {variant_name}"
            default_code = _series_code(series["code_prefix"], counter)
            description = f"{series['description_template']} Variant: {variant_name}."

            records[xml_id] = _build_record(
                xml_id=xml_id,
                name=product_name,
                code=default_code,
                description=description,
                sale_price=sale_price,
                cost_price=cost_price,
            )
            counter += 1

    return records


RECORDS = _build_records()
